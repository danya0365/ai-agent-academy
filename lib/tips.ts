/** คืนเคล็ดลับทั้งหมด (เรียงตาม createdAt desc — ใหม่สุดขึ้นก่อน) */
export function getAllTips(): Tip[] {
  return [...TIPS].sort(
    (a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime(),
  );
}

/** หาเคล็ดลับตาม slug — คืน undefined ถ้าไม่เจอ */
export function getTipBySlug(slug: string): Tip | undefined {
  return TIPS.find((t) => t.slug === slug);
}

/** รายชื่อหมวดทั้งหมด (ไม่ซ้ำ) */
export function getCategories(): string[] {
  return [...new Set(TIPS.map((t) => t.category))];
}
