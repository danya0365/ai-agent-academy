/**
 * เนื้อหาเมนู "รู้ก่อนใคร" — เก็บเป็น data ล้วน (ไม่มี JSX)
 *
 * - title/summary/category = metadata ใช้ในการ์ด + SEO + fallback
 * - sections หรือ content = เนื้อหาสำหรับ fallback renderer (components/tips/tip-body.tsx)
 *   เมื่อ slug นั้นไม่มี component เฉพาะใน components/tips/registry.tsx
 *
 * ⚠️ ห้าม import React component เข้ามาที่นี่ — ให้ไฟล์นี้เป็น data บริสุทธิ์
 * การผูก slug → component เฉพาะตัว อยู่ที่ registry แทน
 */

export type TipSection = {
  heading?: string;
  /** ย่อหน้า (คั่นด้วย \n\n ได้) */
  body?: string;
  /** รายการหัวข้อย่อย */
  bullets?: string[];
};

export type Tip = {
  slug: string;
  title: string;
  summary: string;
  category: string;
  /** เช่น "5 นาที" */
  readingTime?: string;
  /** เนื้อหาแบบมีหัวข้อย่อย (ทางเลือกหลักของ fallback) */
  sections?: TipSection[];
  /** เนื้อหา text ล้วน (ใช้เมื่อไม่ต้องการ sections) */
  content?: string;
  /** แท็กสำหรับจับคู่สินค้า Shopee ที่เกี่ยวข้อง (ดู lib/shopee-products.ts) */
  productTags?: string[];
};

