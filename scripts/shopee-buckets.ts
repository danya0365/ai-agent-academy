/**
 * "ชั้นวางสินค้า" (buckets) สำหรับคัดสินค้าจาก Shopee Product Feed (CSV)
 *
 * แนวคิด: พี่นิยาม "ชั้น" ไว้ (หมวด + คำที่ใช้ค้นในชื่อสินค้า + แท็ก + note)
 * แล้ว scripts/fetch-shopee-products.ts จะ stream อ่านฟีด CSV (ล้านแถว) กรองเฉพาะสินค้า
 * ที่ชื่อ (title) มีคำใน `match` → ให้คะแนน "ขายดี × เรตติ้ง" → เก็บ top ต่อชั้น
 *
 * แก้ได้อิสระ: เพิ่ม/ลบชั้น, เพิ่มคำใน match, ปรับ limit — รัน `npm run shopee:fetch` ใหม่พอ
 * เคล็ด: ใส่คำที่ "เจาะจง" (เช่น "หูฟังบลูทูธ" ดีกว่า "หูฟัง") กันสินค้าหลุดหมวด
 */

export type ShopeeBucket = {
  /** หมวดที่จะโชว์บนหน้า /shop (ตรงกับ ShopeeProduct.category) */
  category: string;
  /** คำที่ต้องพบใน title (เจอคำใดคำหนึ่งก็เข้าหมวด) — เทียบแบบ case-insensitive */
  match: string[];
  /** แท็กสำหรับจับคู่กับ tip.productTags (vocabulary เดิม: ทำงาน/ประสิทธิภาพ/prompt/รายได้/เครื่องมือ) */
  tags: string[];
  /** ข้อความแนะนำต่อหมวด (คง voice "dev แนะนำ") */
  note: string;
  /** จำนวนสินค้าสูงสุดที่จะเก็บจากหมวดนี้ */
  limit: number;
  /** ตั้ง featured ให้สินค้าในหมวดนี้ (fallback เวลา tip ไม่มีแท็กตรงบน /tips) */
  featured?: boolean;
};

